// Relational or comparison operators

// > -more than
var result = 10 > 5
console.log(result) 

// < -less than
var result = 10 < 5
console.log(result) 

// >= -more than or equal
var result = 10 >= 10
console.log(result) 

// <= -less than or equal
var result = 10 <= 5
console.log(result) 

// == -equal
var result = 10 == 10
console.log(result)

// != -not equal
var result = 10 != 5
console.log(result) 


// === -strict equal (value and type)
var x = 1
console.log(x == '1') // loose comparison (we compare value from the left with the value from the right)
console.log(x === '1') // strict comparison (we compare value and type)

